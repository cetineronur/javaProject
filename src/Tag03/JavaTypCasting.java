package Tag03;

public class JavaTypCasting {

	public static void main(String[] args) {
		byte a=127;
		short b=a;
		int c=a;
		float d=a;
		double e=a;
		System.out.println("byte "+a);
		System.out.println("short "+b);
		System.out.println("int "+c);
		System.out.println("float "+d);
		System.out.println("double "+e);
	}

}
